public class DatosRespuesta {

    private String municipio;
    private String porcentaje;
    private String presenciales;
    private String virtuales;

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public String getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(String porcentaje) {
        this.porcentaje = porcentaje;
    }

    public String getPresenciales() {
        return presenciales;
    }

    public void setPresenciales(String presenciales) {
        this.presenciales = presenciales;
    }

    public String getVirtuales() {
        return virtuales;
    }

    public void setVirtuales(String virtuales) {
        this.virtuales = virtuales;
    }


}
